/** RELATIONAL OPERATORS */
/**
 * Relational operators in Java are binary operators used to compare two values or expressions,
 * always returning a boolean result (true or false). These comparison tools are fundamental for making decisions.
 */
class relational{
  public static void main(String[] args){
    int a = 7;
    int b = 10;
    System.out.println(">: "+ (a>b) );
    System.out.println("<: "+ (a<b) );
    System.out.println("==: "+ (a==b) );
    System.out.println("!=: "+ (a!=b) );
    System.out.println(">=: "+ (a>=b) );
    System.out.println("<=: "+ (a<=b) );
  }
}